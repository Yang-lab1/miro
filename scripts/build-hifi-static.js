const fs = require("fs/promises");
const path = require("path");

const ROOT_DIR = path.resolve(__dirname, "..");
const DIST_DIR = path.join(ROOT_DIR, "dist");
const PROTOTYPE_HTML = path.join(
  ROOT_DIR,
  "docs",
  "exploration",
  "ui",
  "hifi",
  "miro-hifi-prototype.html"
);

const DEFAULT_API_BASE = "/api/v1";

function readEnv(name, fallback = "") {
  const value = process.env[name];
  return typeof value === "string" && value.trim() ? value.trim() : fallback;
}

function runtimeConfig() {
  const apiBase = readEnv("MIRO_API_BASE", DEFAULT_API_BASE);
  const reviewApiBase = readEnv("MIRO_REVIEW_API_BASE", apiBase);
  const supabaseUrl = readEnv("MIRO_SUPABASE_URL", "");
  const supabasePublishableKey = readEnv("MIRO_SUPABASE_PUBLISHABLE_KEY", "");
  const supabaseAuthRedirectTo = readEnv("MIRO_SUPABASE_AUTH_REDIRECT_TO", "");
  const turnstileSiteKey = readEnv("MIRO_TURNSTILE_SITE_KEY", "");

  return `// Generated by scripts/build-hifi-static.js
globalThis.MIRO_API_BASE ??= ${JSON.stringify(apiBase)};
globalThis.MIRO_REVIEW_API_BASE ??= ${JSON.stringify(reviewApiBase)};
globalThis.MIRO_SUPABASE_URL ??= ${JSON.stringify(supabaseUrl)};
globalThis.MIRO_SUPABASE_PUBLISHABLE_KEY ??= ${JSON.stringify(supabasePublishableKey)};
globalThis.MIRO_SUPABASE_AUTH_REDIRECT_TO ??= ${JSON.stringify(supabaseAuthRedirectTo)};
globalThis.MIRO_TURNSTILE_SITE_KEY ??= ${JSON.stringify(turnstileSiteKey)};
`;
}

async function copyFile(source, destination) {
  await fs.mkdir(path.dirname(destination), { recursive: true });
  await fs.copyFile(source, destination);
}

async function main() {
  await fs.rm(DIST_DIR, { recursive: true, force: true });
  await fs.mkdir(DIST_DIR, { recursive: true });

  const html = await fs.readFile(PROTOTYPE_HTML, "utf8");
  const deployHtml = html.replace(
    '<script src="../../../../runtime-config.js"></script>',
    '<script src="runtime-config.js"></script>'
  );

  await fs.writeFile(path.join(DIST_DIR, "index.html"), deployHtml, "utf8");
  await fs.writeFile(path.join(DIST_DIR, "runtime-config.js"), runtimeConfig(), "utf8");

  await copyFile(
    path.join(ROOT_DIR, "docs", "exploration", "ui", "assets", "world-map-dark.svg"),
    path.join(DIST_DIR, "assets", "world-map-dark.svg")
  );
  await copyFile(
    path.join(ROOT_DIR, "docs", "exploration", "ui", "assets", "world-map.svg"),
    path.join(DIST_DIR, "assets", "world-map.svg")
  );
  await copyFile(
    path.join(ROOT_DIR, "src", "assets", "logo.png"),
    path.join(DIST_DIR, "src", "assets", "logo.png")
  );
  await copyFile(
    path.join(ROOT_DIR, "src", "assets", "export.png"),
    path.join(DIST_DIR, "src", "assets", "export.png")
  );

  process.stdout.write("Built high-fidelity static prototype into dist\\n");
}

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
