const fs = require("fs/promises");
const path = require("path");

const ROOT_DIR = path.resolve(__dirname, "..");
const OUTPUT_PATH = path.join(ROOT_DIR, "runtime-config.js");
const DEFAULT_API_BASE = "/api/v1";

function readEnv(name, fallback = "") {
  const value = process.env[name];
  return typeof value === "string" && value.trim() ? value.trim() : fallback;
}

function toRuntimeLiteral(value) {
  return JSON.stringify(value);
}

async function main() {
  const apiBase = readEnv("MIRO_API_BASE", DEFAULT_API_BASE);
  const reviewApiBase = readEnv("MIRO_REVIEW_API_BASE", apiBase);
  const supabaseUrl = readEnv("MIRO_SUPABASE_URL", "");
  const supabasePublishableKey = readEnv("MIRO_SUPABASE_PUBLISHABLE_KEY", "");
  const supabaseAuthRedirectTo = readEnv("MIRO_SUPABASE_AUTH_REDIRECT_TO", "");
  const turnstileSiteKey = readEnv("MIRO_TURNSTILE_SITE_KEY", "");

  const payload = `// Generated by scripts/generate-runtime-config.js
globalThis.MIRO_API_BASE ??= ${toRuntimeLiteral(apiBase)};
globalThis.MIRO_REVIEW_API_BASE ??= ${toRuntimeLiteral(reviewApiBase)};
globalThis.MIRO_SUPABASE_URL ??= ${toRuntimeLiteral(supabaseUrl)};
globalThis.MIRO_SUPABASE_PUBLISHABLE_KEY ??= ${toRuntimeLiteral(
    supabasePublishableKey
)};
globalThis.MIRO_SUPABASE_AUTH_REDIRECT_TO ??= ${toRuntimeLiteral(
    supabaseAuthRedirectTo
)};
globalThis.MIRO_TURNSTILE_SITE_KEY ??= ${toRuntimeLiteral(
    turnstileSiteKey
)};
`;

  await fs.writeFile(OUTPUT_PATH, payload, "utf8");
  process.stdout.write(`Wrote ${path.basename(OUTPUT_PATH)}\n`);
}

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
